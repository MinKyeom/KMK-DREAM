# 내부에 작은 따옴표 넣기

print("'배가 고픕니다' ")

# 내부에 큰 따옴표 넣기

print('"배가 고픕니다"')

# 단순히 이스케이프 문자를 활용하여 문자 표시

print("\'배가 고픕니다")

print('\"배가 고픕니다.')

# \n, \t

print("안녕하세요\n안녕하세요")

print("안녕하세요\t안녕하세요")

# 줄바꿈 없이 문자열 만들기

print("""
동해물과 백두산이
마르고 닳도록 """)
print() #구분을 위하여
print("""\
동해물과 백두산이
마르고 닳도록\
""")

# 문자열 반복

print(3*"안녕")
print("안녕"*3)

#문자열 선택

print("안녕하세요"[1:4])  #마지막 글자는 포함하지 않게 출력된다는 점!

#문자열 길이 구하기

print(len("안녕하세요"))
